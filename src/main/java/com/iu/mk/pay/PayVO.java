package com.iu.mk.pay;

public class PayVO {
	private long pq_num;
	private long cq_num;
	private long pay_price;
	private long order_num;
	
	
	public long getPq_num() {
		return pq_num;
	}
	public void setPq_num(long pq_num) {
		this.pq_num = pq_num;
	}
	public long getCq_num() {
		return cq_num;
	}
	public void setCq_num(long cq_num) {
		this.cq_num = cq_num;
	}
	public long getPay_price() {
		return pay_price;
	}
	public void setPay_price(long pay_price) {
		this.pay_price = pay_price;
	}
	public long getOrder_num() {
		return order_num;
	}
	public void setOrder_num(long order_num) {
		this.order_num = order_num;
	}
	
	
	
	
}
